SHARE_TEXT_TEMPLATE = (
    "Я прошёл зооэкспедицию и познакомился со своим внутренним спутником — это {animal_name}.\n\n"
    "Узнай своё тотемное животное: {bot_link}"
)


RESULT_TEMPLATE = (
    "{result_title}\n\n"
    "{description}"
)


def get_empty_scores() -> dict[str, int]:
    """
    Возвращает стартовый словарь баллов.
    Используется при начале новой викторины.
    """
    return {animal_id: 0 for animal_id in ANIMAL_ORDER}


def build_result_text(animal_id: str) -> str:
    """
    Собирает итоговый текст результата по id животного.
    """
    animal = ANIMALS[animal_id]

    return RESULT_TEMPLATE.format(
        result_title=animal["result_title"],
        description=animal["description"],
    )


def build_share_text(animal_id: str, bot_link: str) -> str:
    """
    Собирает текст, который пользователь может скопировать
    и отправить в соцсети.
    """
    animal = ANIMALS[animal_id]

    return SHARE_TEXT_TEMPLATE.format(
        animal_name=animal["name"],
        bot_link=bot_link,
    )
